package GenshinDB;

public class GenshinVO {
	private String name;
	private String element;
	private String hoshi;
	private String wepwon;
	private String levels;
	private String picture;
	private String info;
	private int ascend;
	
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getElement() {
		return element;
	}
	public void setElement(String element) {
		this.element = element;
	}
	public String getHoshi() {
		return hoshi;
	}
	public void setHoshi(String hoshi) {
		this.hoshi = hoshi;
	}
	public String getWepwon() {
		return wepwon;
	}
	public void setWepwon(String wepwon) {
		this.wepwon = wepwon;
	}
	public String getLevels() {
		return levels;
	}
	public void setLevels(String levels) {
		this.levels = levels;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public int getAscend() {
		return ascend;
	}
	public void setAscend(int ascend) {
		this.ascend = ascend;
	}
	
	
}
