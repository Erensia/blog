package StarrailDB;

public class StarrailVO {
	String name;
	String element;
	String michi;
	String hoshi;
	String wepwon;
	int levels;
	String picture;
	String info;
	int ascend;
	
	public String getElement() {
		return element;
	}
	public void setElement(String element) {
		this.element = element;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMichi() {
		return michi;
	}
	public void setMichi(String michi) {
		this.michi = michi;
	}
	public String getHoshi() {
		return hoshi;
	}
	public void setHoshi(String hoshi) {
		this.hoshi = hoshi;
	}
	public String getWepwon() {
		return wepwon;
	}
	public void setWepwon(String wepwon) {
		this.wepwon = wepwon;
	}
	public int getLevels() {
		return levels;
	}
	public void setLevels(int levels) {
		this.levels = levels;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public int getAscend() {
		return ascend;
	}
	public void setAscend(int ascend) {
		this.ascend = ascend;
	}
	
	
}
